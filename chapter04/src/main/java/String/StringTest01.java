package String;

public class StringTest01 {

	public static void main(String[] args) {

		// c: emp
		System.out.println("c:\temp");

		System.out.println("c:\\temp");
		// c:\temp
		
		
		
		System.out.println("\"hello\"");
		//"hello"
		
		// \t : tab
		// \r : carrige return
		// \n : newline
		// \b : beep
		
		System.out.println("hello\tworld\nenter");
		
		// ' : \'
		char c = '\'';
		System.out.println(c);
		System.out.println("\'");
		
		
	}

}
